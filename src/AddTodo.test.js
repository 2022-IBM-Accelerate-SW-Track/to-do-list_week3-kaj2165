 test('test that App component renders different colors for past due events', () => {
  render(<App />);
  const inputTask = screen.getByRole('textbox', {name: /Add New Item/i});
  const inputDate = screen.getByPlaceholderText("mm/dd/yyyy");
  const element = screen.getByRole('button', {name: /Add/i});
  const dueDate = "05/30/2023";
  const lateDueDate = "03/01/2022";
  fireEvent.change(inputTask, { target: { value: "History Test"}});
  fireEvent.change(inputDate, { target: { value: dueDate}});
  fireEvent.click(element);
  fireEvent.change(inputTask, { target: { value: "CS Test"}});
  fireEvent.change(inputDate, { target: { value: lateDueDate}});
  fireEvent.click(element); 
  const item1 = screen.getByText(/History Test/i);
  const item2 = screen.getByText(/CS Test/i);
  const style1 = window.getComputedStyle(item1);
  const style2 = window.getComputedStyle(item2);
  expect(style1).not.toEqual(style2);
 });
